numero_dia = int(input('Digite um numero de 1 a 7: '))

if numero_dia == 1:
    print('1 é Domingo')

elif numero_dia == 2:
    print('2 é Segunda-Feira')

elif numero_dia == 3:
    print('3 é Terça-Feira')

elif numero_dia == 4:
    print('4 é Quarta-Feira')

elif numero_dia == 5:
    print('5 é Quinta-Feira')

elif numero_dia == 6:
    print('6 é Sexta-Feira')

elif numero_dia == 7:
    print('7 é Sabado')


else:
    print('Numero errado, digite um numero e 1 a 7')